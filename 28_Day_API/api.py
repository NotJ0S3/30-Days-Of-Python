# # 1. Read about API and HTTP

# API (Application Programming Interface)
# An API is like a bridge that allows two software systems to talk to each other.

# What it does: It defines rules and methods that applications can use to request and send data.
# Examples:
# When you use an app to check the weather, it might use a weather API to fetch the data from a server.
# Social media platforms offer APIs for developers to integrate features like posting updates or retrieving user data.
# Think of an API as a waiter in a restaurant. The waiter takes your order (request), passes it to the kitchen (server), and brings back your food (response).

# HTTP (HyperText Transfer Protocol)
# HTTP is the protocol (or set of rules) used to exchange information over the internet.

# What it does: It defines how requests and responses are structured and transferred between a client (like a browser or app) and a server.
# Examples:
# When you type a URL in your browser, your browser sends an HTTP request to the server, which then responds with the web page.
# API calls often use HTTP to send requests and receive responses.
# How they work together
# APIs often use HTTP as the communication method. For example:

# A weather app sends an HTTP request to a weather API (e.g., GET https://weatherapi.com/current).
# The server processes the request and responds with data (e.g., temperature, humidity) in a format like JSON or XML.
# The app displays the weather information.